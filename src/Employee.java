
public class Employee {
	
	private String name;
	private int id;
	
	public Employee(final String name,final int id) {
		this.name = name;
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	public int getId() {
		return id;
	}
	
	public void setName(final String name) {
		this.name = name;
	}
	public void setId(final int id) {
		this.id = id;
	}
	

}
